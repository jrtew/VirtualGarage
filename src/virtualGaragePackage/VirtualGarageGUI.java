    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        // The login button checks the username/password combo, then takes 
        // the user to their virtual garage:
        String username = usernameCB.getSelectedItem().toString();
        String password = passwordTB.getText();
        // Check user/pass combo
        if (fileFacade.checkLogin("userList", username, password)) {
            currentUser = username;
            loginPanel.setVisible(false);
            vehiclePanel.setVisible(true);
            vehicleList = fileFacade.getUserVehicleList("vehicleList", currentUser);
            vehicleCB.removeAllItems();
            for (int i = 0; i < vehicleList.length; i++) {
                vehicleCB.addItem(vehicleList[i]);
            }
            passwordTB.setText("");
        } else {
            // Display message if login info is incorrect:
            JOptionPane.showMessageDialog(null, "Password incorrect. Please try again.");
            passwordTB.setText("");
        }
    }//GEN-LAST:event_loginButtonActionPerformed

    private void createNewUserButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createNewUserButtonActionPerformed
        // Method for when the create new user button is pressed. Adds the entered
        // username and password to the userList text file in the project files:
        if (newUsernameTB.getText().isEmpty() || newPasswordTB.getText().isEmpty()) {
            // Make sure both fields entered:
            JOptionPane.showMessageDialog(null, "Please enter both a Username and Password.");
        } else {
            String newUser = newUsernameTB.getText().toString();
            String newPass = newPasswordTB.getText().toString();
            String[] newUserRow = {newUser, newPass};

            fileFacade.writeNewUsers("userList", newUserRow);
            String[] tempUserList = fileFacade.getUserList("userList");
            usernameCB.removeAllItems();
            for (int i = 0; i < tempUserList.length; i++) {
                usernameCB.addItem(tempUserList[i]);
            }

            newUsernameTB.setText("");
            newPasswordTB.setText("");
        }
    }//GEN-LAST:event_createNewUserButtonActionPerformed

    private void goToLogButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_goToLogButtonActionPerformed
        // This button takes the user to their maintenance log for the selected
        // vehicle. The log displays their info in a jTable using the AbstractTableModel:
        fileName = vehicleCB.getSelectedItem().toString();
        currentVehicle = vehicleCB.getSelectedItem().toString();

        if (!holdingList.isEmpty()) {
            holdingList.clear();
        }
        holdingList.addAll(tableFactory.createTable(AbstractTableFactory.TableType.MAINTENANCE, fileName));
        myModel.newDataNewTable(holdingList);
        maintTable.setModel(myModel);
        myModel.fireTableDataChanged();

        String[] makeModelYear = fileFacade.returnMakeModelYear("vehicleList", currentVehicle);
        userTextField.setText(currentUser);
        makeTextField.setText(makeModelYear[0]);
        modelTextField.setText(makeModelYear[1]);
        yearTextField.setText(makeModelYear[2]);

        loginPanel.setVisible(false);
        vehiclePanel.setVisible(false);
        maintenanceLogPanel.setVisible(true);
    }//GEN-LAST:event_goToLogButtonActionPerformed

    private void createNewVehicleButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createNewVehicleButtonActionPerformed
        // Button for creating new vehicle info:
        if (newMakeTB.getText().isEmpty() || newModelTB.getText().isEmpty() || newYearTB.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter text for all three fields.");
        } else {
            String newMake = newMakeTB.getText().toString();
            String newModel = newModelTB.getText().toString();
            String newYear = newYearTB.getText().toString();
            String[] newVehicleRow = {newMake, newModel, newYear, currentUser};
            fileFacade.writeNewVehicle("vehicleList", newVehicleRow);

            String[] tempVehicleRow = fileFacade.getUserVehicleList("vehicleList", currentUser);
            vehicleCB.removeAllItems();
            for (int i = 0; i < tempVehicleRow.length; i++) {
                vehicleCB.addItem(tempVehicleRow[i]);
            }
            fileFacade.writeBlankFile(newModel);

            newMakeTB.setText("");
            newModelTB.setText("");
            newYearTB.setText("");
        }
    }//GEN-LAST:event_createNewVehicleButtonActionPerformed


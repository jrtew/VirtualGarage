    private void addRowButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addRowButtonActionPerformed
        // Adds the information from the text fields to the table:
        if (dateTB.getText().isEmpty() || milesTB.getText().isEmpty() || maintTB.getText().isEmpty()
                || commentsTB.getText().isEmpty() || costTB.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter text for all fields.");
        } else {
            TableData newMaintRow = new MaintenanceData(dateTB.getText(),
                    milesTB.getText(), maintTB.getText(), commentsTB.getText(), costTB.getText());
            myModel.addRow(newMaintRow);
            dateTB.setText("");
            milesTB.setText("");
            maintTB.setText("");
            commentsTB.setText("");
            costTB.setText("");
        }
    }//GEN-LAST:event_addRowButtonActionPerformed

